/**
 * Cross-runtime proof that on-request dev regeneration (#967) behaves
 * identically on Node and Bun. WebJs runs on Node 24+ OR Bun (#508), and this
 * feature exercises runtime-sensitive surfaces: `child_process.spawn` with
 * `shell: true` + a `node_modules/.bin`-augmented PATH, and `fs/promises`
 * stat/readdir mtime walking. Run from the repo root:
 *
 *   node test/bun/dev-regenerate.mjs
 *   bun  test/bun/dev-regenerate.mjs
 *
 * Asserts, on whichever runtime executes it: a STALE output is rebuilt by the
 * declared command (real spawn), and a FRESH output is left untouched. The
 * regenerate command is a portable POSIX `printf > file`, so it needs no
 * compiler and no `node`/`bun` on PATH.
 */
import assert from 'node:assert/strict';
import { mkdtempSync, mkdirSync, writeFileSync, readFileSync, utimesSync } from 'node:fs';
import { tmpdir } from 'node:os';
import { join } from 'node:path';

import { maybeRegenerate, readRegenerateRules, _resetInFlight } from '../../packages/server/src/dev-regenerate.js';

const runtime = process.versions.bun ? `bun ${process.versions.bun}` : `node ${process.versions.node}`;

function app(pkg) {
  const dir = mkdtempSync(join(tmpdir(), 'webjs-967-bun-'));
  mkdirSync(join(dir, 'app'), { recursive: true });
  mkdirSync(join(dir, 'public'), { recursive: true });
  writeFileSync(join(dir, 'package.json'), JSON.stringify(pkg));
  writeFileSync(join(dir, 'public', 'input.css'), '@import "tailwindcss";');
  return dir;
}

const RULE = {
  output: 'public/tailwind.css',
  // Portable POSIX regenerate command: emit a marker, no compiler needed.
  command: `printf '.grid-cols-9{display:grid}' > public/tailwind.css`,
  inputs: ['app', 'public/input.css'],
};
const pkg = { name: 'x', webjs: { dev: { regenerate: [RULE] } } };

// readRegenerateRules parses + normalizes the same on both runtimes.
{
  const dir = app(pkg);
  const rules = await readRegenerateRules(dir);
  assert.equal(rules.length, 1, `[${runtime}] one rule parsed`);
  assert.equal(rules[0].output, 'public/tailwind.css', `[${runtime}] output normalized`);
}

// STALE output (a source is newer): the command runs via real spawn.
{
  _resetInFlight();
  const dir = app(pkg);
  writeFileSync(join(dir, 'public', 'tailwind.css'), '/* stale */');
  const past = new Date(Date.now() - 60_000);
  utimesSync(join(dir, 'public', 'tailwind.css'), past, past);
  utimesSync(join(dir, 'public', 'input.css'), past, past);
  writeFileSync(join(dir, 'app', 'page.ts'), 'export default () => "grid-cols-9";'); // edited now
  const rules = await readRegenerateRules(dir);
  await maybeRegenerate(dir, 'public/tailwind.css', rules);
  const css = readFileSync(join(dir, 'public', 'tailwind.css'), 'utf8');
  assert.match(css, /grid-cols-9/, `[${runtime}] stale output was regenerated by the spawned command`);
}

// FRESH output (newer than every source): the command must NOT run.
{
  _resetInFlight();
  const dir = app({
    name: 'x',
    webjs: { dev: { regenerate: [{
      output: 'public/tailwind.css',
      command: `printf 'SHOULD_NOT_RUN' > public/tailwind.css`,
      inputs: ['app', 'public/input.css'],
    }] } },
  });
  writeFileSync(join(dir, 'app', 'page.ts'), 'export default () => 1;');
  const past = new Date(Date.now() - 60_000);
  utimesSync(join(dir, 'app', 'page.ts'), past, past);
  utimesSync(join(dir, 'public', 'input.css'), past, past);
  writeFileSync(join(dir, 'public', 'tailwind.css'), '.fresh{x:1}'); // written now => newest
  const rules = await readRegenerateRules(dir);
  await maybeRegenerate(dir, 'public/tailwind.css', rules);
  const css = readFileSync(join(dir, 'public', 'tailwind.css'), 'utf8');
  assert.doesNotMatch(css, /SHOULD_NOT_RUN/, `[${runtime}] fresh output was not rebuilt`);
  assert.match(css, /fresh/, `[${runtime}] fresh output preserved`);
}

console.log(`[dev-regenerate] #967 on-request regeneration OK on ${runtime}`);
